package array_test;

public class ArrayDemo2 {
    public static void main(String[] args) {
//        动态初始化数组
        int ages[] = new int[5];
        ages[0] = 16;
        System.out.println(ages[0]);
        System.out.println(ages[1]);

        String names[] = new String[5];
        names[0] = "andy";
        System.out.println(names[0]);
        System.out.println(names[1]);
    }
}
